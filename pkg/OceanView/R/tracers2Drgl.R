tracers2Drgl <- function(x, y, colvar = NULL, ..., 
  col = NULL, NAcol = "white", clim = NULL) {
   z <- rep (1.001, length.out = length(x))
   tracers3Drgl(x, y, z, colvar = colvar, 
     col = col, NAcol = NAcol, clim = clim, ...)
}

